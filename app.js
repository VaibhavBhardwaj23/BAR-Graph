// MONDAY
function dis1()
{
    document.querySelector(".monday").classList.remove("hide")
}
function hid1()
{
    document.querySelector(".monday").classList.add("hide")
}
function per1()
{
    document.querySelector(".M").classList.toggle("blue");
        document.querySelector(".monday").classList.remove("hide")

}
